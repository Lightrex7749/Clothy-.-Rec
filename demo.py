from ultralytics import YOLO

pose_model = YOLO("yolov8s-pose.pt")

def crop_top_bottom_from_person(person_path):
    img_bgr = cv2.imread(person_path)
    if img_bgr is None:
        raise ValueError("Could not read image.")
    H, W = img_bgr.shape[:2]

    res = pose_model.predict(person_path, conf=0.25, verbose=False)[0]
    if res.keypoints is None or len(res.keypoints) == 0:
        raise ValueError("No person detected. Upload a clearer full-body standing photo.")

    best_i = int(np.argmax(res.boxes.conf.cpu().numpy()))
    kpts = res.keypoints.xy[best_i].cpu().numpy()
    x1, y1, x2, y2 = res.boxes.xyxy[best_i].cpu().numpy()
    x1, y1, x2, y2 = map(int, [x1,y1,x2,y2])

    # COCO indices
    L_SH, R_SH = 5, 6
    L_HIP, R_HIP = 11, 12
    L_ANK, R_ANK = 15, 16

    def clampi(v, lo, hi): return int(max(lo, min(hi, v)))
    def safe_xy(points):
        pts = points.copy()
        good = ~np.isnan(pts[:,0]) & ~np.isnan(pts[:,1]) & (pts[:,0] > 1) & (pts[:,1] > 1)
        return pts[good] if good.any() else pts

    shoulders = safe_xy(kpts[[L_SH, R_SH]])
    hips      = safe_xy(kpts[[L_HIP, R_HIP]])
    ankles    = safe_xy(kpts[[L_ANK, R_ANK]])

    if shoulders.size and hips.size:
        top_y1 = min(shoulders[:,1].min(), hips[:,1].min())
        top_y2 = hips[:,1].max()
        top_x1 = min(shoulders[:,0].min(), hips[:,0].min())
        top_x2 = max(shoulders[:,0].max(), hips[:,0].max())
    else:
        top_x1, top_y1, top_x2, top_y2 = x1, y1, x2, int((y1+y2)/2)

    if hips.size and ankles.size:
        bot_y1 = hips[:,1].min()
        bot_y2 = ankles[:,1].max()
        bot_x1 = min(hips[:,0].min(), ankles[:,0].min())
        bot_x2 = max(hips[:,0].max(), ankles[:,0].max())
    else:
        bot_x1, bot_y1, bot_x2, bot_y2 = x1, int((y1+y2)/2), x2, y2

    pad_x = int(0.10 * (x2-x1))
    pad_y = int(0.08 * (y2-y1))

    top_x1 = clampi(top_x1-pad_x, 0, W-1); top_x2 = clampi(top_x2+pad_x, 0, W-1)
    top_y1 = clampi(top_y1-pad_y, 0, H-1); top_y2 = clampi(top_y2+pad_y, 0, H-1)

    bot_x1 = clampi(bot_x1-pad_x, 0, W-1); bot_x2 = clampi(bot_x2+pad_x, 0, W-1)
    bot_y1 = clampi(bot_y1-pad_y, 0, H-1); bot_y2 = clampi(bot_y2+pad_y, 0, H-1)

    top_crop = img_bgr[top_y1:top_y2, top_x1:top_x2].copy()
    bot_crop = img_bgr[bot_y1:bot_y2, bot_x1:bot_x2].copy()

    top_path = "/content/top_crop.jpg"
    bot_path = "/content/bottom_crop.jpg"
    cv2.imwrite(top_path, top_crop)
    cv2.imwrite(bot_path, bot_crop)

    return top_path, bot_path

