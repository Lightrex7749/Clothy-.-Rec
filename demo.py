@torch.no_grad()
def clip_embed_one(img_path):
    x = clip_preprocess(Image.open(img_path).convert("RGB")).unsqueeze(0).to(device)
    f = clip_model.encode_image(x)
    f = f / f.norm(dim=-1, keepdim=True)
    return f.cpu().numpy().astype("float32")

@torch.no_grad()
def clip_text_embed_one(text):
    tokens = tokenizer([text]).to(device)
    t = clip_model.encode_text(tokens)
    t = t / t.norm(dim=-1, keepdim=True)
    return t.cpu().numpy().astype("float32")

@torch.no_grad()
def classify_both(img_path):
    x = eval_tf(Image.open(img_path).convert("RGB")).unsqueeze(0).to(device)
    lr = resnet(x)
    le = effnet(x)

    pr = torch.softmax(lr,1)[0].cpu().numpy()
    pe = torch.softmax(le,1)[0].cpu().numpy()
    pens = torch.softmax((lr+le)/2.0,1)[0].cpu().numpy()

    def pack(p):
        pred_id = int(p.argmax())
        return {"pred_id": pred_id, "pred_label": id2label[pred_id], "conf": float(p.max())}

    return pack(pr), pack(pe), pack(pens)

