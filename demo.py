@torch.no_grad()
def collect_outputs(model, loader):
    model.eval()
    ys, preds, probs = [], [], []
    for x, y in loader:
        x = x.to(device)
        logits = model(x)
        p = torch.softmax(logits, dim=1).cpu().numpy()  # (B,C)
        pred = p.argmax(axis=1)

        ys.append(y.numpy())
        preds.append(pred)
        probs.append(p)

    y_true = np.concatenate(ys)
    y_pred = np.concatenate(preds)
    y_prob = np.vstack(probs)
    return y_true, y_pred, y_prob

def plot_confusion(y_true, y_pred, labels, title, normalize=False, save_path=None):
    cm = confusion_matrix(y_true, y_pred)
    if normalize:
        cm = cm.astype(float) / cm.sum(axis=1, keepdims=True)

    plt.figure(figsize=(9,7))
    sns.heatmap(
        cm, cmap="Blues",
        xticklabels=labels, yticklabels=labels,
        annot=False, fmt=".2f" if normalize else "d"
    )
    plt.title(title + (" (Normalized)" if normalize else ""))
    plt.xlabel("Predicted"); plt.ylabel("True")
    plt.tight_layout()
    if save_path: plt.savefig(save_path, dpi=200)
    plt.show()

def plot_per_class_f1(y_true, y_pred, labels, title, save_path=None):
    f1s = f1_score(y_true, y_pred, average=None)
    dfm = pd.DataFrame({"class": labels, "F1": f1s}).sort_values("F1", ascending=False)

    plt.figure(figsize=(10,4))
    sns.barplot(data=dfm, x="class", y="F1", color="#4f46e5")
    plt.xticks(rotation=35, ha="right")
    plt.ylim(0, 1)
    plt.title(title + " — Per-class F1")
    plt.tight_layout()
    if save_path: plt.savefig(save_path, dpi=200)
    plt.show()

def plot_prf_heatmap(y_true, y_pred, labels, title, save_path=None):
    p = precision_score(y_true, y_pred, average=None, zero_division=0)
    r = recall_score(y_true, y_pred, average=None, zero_division=0)
    f = f1_score(y_true, y_pred, average=None, zero_division=0)

    mat = pd.DataFrame({"precision": p, "recall": r, "f1": f}, index=labels)
    plt.figure(figsize=(6, len(labels)*0.45 + 1))
    sns.heatmap(mat, annot=True, cmap="YlGnBu", fmt=".2f")
    plt.title(title + " — Precision/Recall/F1 by class")
    plt.tight_layout()
    if save_path: plt.savefig(save_path, dpi=200)
    plt.show()

def plot_confidence_hist(y_true, y_pred, y_prob, title, save_path=None):
    conf = y_prob.max(axis=1)
    correct = (y_true == y_pred)

    plt.figure(figsize=(8,4))
    sns.histplot(conf[correct], color="green", bins=20, stat="density", label="Correct", alpha=0.55)
    sns.histplot(conf[~correct], color="red", bins=20, stat="density", label="Wrong", alpha=0.55)
    plt.title(title + " — Confidence distribution")
    plt.xlabel("Max softmax probability")
    plt.legend()
    plt.tight_layout()
    if save_path: plt.savefig(save_path, dpi=200)
    plt.show()

def plot_reliability_curve(y_true, y_prob, title, n_bins=10, save_path=None):
    conf = y_prob.max(axis=1)
    pred = y_prob.argmax(axis=1)
    correct = (pred == y_true).astype(float)

    bins = np.linspace(0, 1, n_bins+1)
    bin_ids = np.digitize(conf, bins) - 1

    accs, confs = [], []
    for b in range(n_bins):
        m = bin_ids == b
        if m.sum() == 0:
            continue
        accs.append(correct[m].mean())
        confs.append(conf[m].mean())

    plt.figure(figsize=(5,5))
    plt.plot([0,1],[0,1],"k--",label="Perfect calibration")
    plt.plot(confs, accs, marker="o", label="Model")
    plt.title(title + " — Reliability curve")
    plt.xlabel("Avg confidence")
    plt.ylabel("Accuracy in bin")
    plt.legend()
    plt.tight_layout()
    if save_path: plt.savefig(save_path, dpi=200)
    plt.show()

def plot_multiclass_roc(y_true, y_prob, labels, title, save_path=None):
    Y = label_binarize(y_true, classes=np.arange(len(labels)))

    plt.figure(figsize=(7,6))
    for i, cls in enumerate(labels):
        fpr, tpr, _ = roc_curve(Y[:, i], y_prob[:, i])
        roc_auc = auc(fpr, tpr)
        plt.plot(fpr, tpr, linewidth=1.2, label=f"{cls} (AUC={roc_auc:.2f})")

    plt.plot([0,1],[0,1],"k--",linewidth=1)
    plt.title(title + " — ROC (OvR)")
    plt.xlabel("FPR"); plt.ylabel("TPR")
    plt.legend(fontsize=8, bbox_to_anchor=(1.02, 1), loc="upper left")
    plt.tight_layout()
    if save_path: plt.savefig(save_path, dpi=200)
    plt.show()

