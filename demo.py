import cv2, numpy as np

def clothing_color_stats(img_path, resize=180):
    img = np.array(Image.open(img_path).convert("RGB"))
    h, w = img.shape[:2]
    scale = resize / max(h, w)
    img = cv2.resize(img, (int(w*scale), int(h*scale)))

    hsv = cv2.cvtColor(img, cv2.COLOR_RGB2HSV)
    H, S, V = hsv[:,:,0], hsv[:,:,1], hsv[:,:,2]

    mask = (S > 35) & (V < 245)
    if mask.sum() < 200:
        mask = np.ones_like(S, dtype=bool)

    return float(H[mask].mean()), float(S[mask].mean()), float(V[mask].mean())

def hue_dist(h1, h2):
    d = abs(h1 - h2)
    return min(d, 180 - d)

def color_harmony_score(top_stats, bottom_stats):
    ht, st, vt = top_stats
    hb, sb, vb = bottom_stats

    neutral_bonus = 1.0 - min(sb/255.0, 1.0)
    if st > 90:  # colorful top => prefer neutral bottoms more
        neutral_bonus *= 1.3

    v_contrast = min(abs(vt - vb)/255.0, 1.0)
    hue_penalty = (hue_dist(ht, hb)/90.0) * (sb/255.0)

    return float(0.55*neutral_bonus + 0.35*v_contrast - 0.20*hue_penalty)

def color_family_from_hsv(h, s, v):
    if s < 45:
        return "neutral"
    if (h <= 35) or (h >= 160) or (35 < h <= 80):
        return "warm"
    return "cool"

def skin_match_score(item_hsv_stats, undertone):
    h, s, v = item_hsv_stats
    fam = color_family_from_hsv(h, s, v)
    if fam == "neutral":
        return 0.95
    if undertone == "neutral":
        return 0.80
    return 1.00 if fam == undertone else 0.45

def recommend_bottoms_free_text(top_img_path, occasion_text, k=5, search_k=120, w_img=0.70, w_txt=0.30):
    occasion_text = (occasion_text or "").strip() or "casual everyday"

    q_img = clip_embed_one(top_img_path)
    img_scores, idxs = index_bottoms.search(q_img, search_k)
    picked_meta_rows = bottoms_meta_idx[idxs[0]]

    cand = meta.iloc[picked_meta_rows].copy()
    cand["img_score"] = img_scores[0]

    templates = [
        f"men's {occasion_text} outfit pants",
        f"men's {occasion_text} trousers",
        f"men's {occasion_text} jeans outfit",
    ]
    t = np.mean([clip_text_embed_one(t) for t in templates], axis=0)
    t = t / np.linalg.norm(t, axis=1, keepdims=True)

    cand_emb = emb_all[picked_meta_rows]
    cand["txt_score"] = (cand_emb @ t.T).reshape(-1)

    cand["final_score"] = w_img*cand["img_score"] + w_txt*cand["txt_score"]
    cand = cand.sort_values("final_score", ascending=False).head(k).reset_index(drop=True)
    cand = cand.rename(columns={"final_score":"score"})
    return cand[["path","label","img_score","txt_score","score"]]

def recommend_tops_free_text(bottom_img_path, occasion_text, k=5, search_k=120, w_img=0.70, w_txt=0.30):
    occasion_text = (occasion_text or "").strip() or "casual everyday"

    q_img = clip_embed_one(bottom_img_path)
    img_scores, idxs = index_tops.search(q_img, search_k)
    picked_meta_rows = tops_meta_idx[idxs[0]]

    cand = meta.iloc[picked_meta_rows].copy()
    cand["img_score"] = img_scores[0]

    templates = [
        f"men's {occasion_text} shirt",
        f"men's {occasion_text} t-shirt",
        f"men's {occasion_text} hoodie",
    ]
    t = np.mean([clip_text_embed_one(t) for t in templates], axis=0)
    t = t / np.linalg.norm(t, axis=1, keepdims=True)

    cand_emb = emb_all[picked_meta_rows]
    cand["txt_score"] = (cand_emb @ t.T).reshape(-1)

    cand["final_score"] = w_img*cand["img_score"] + w_txt*cand["txt_score"]
    cand = cand.sort_values("final_score", ascending=False).head(k).reset_index(drop=True)
    cand = cand.rename(columns={"final_score":"score"})
    return cand[["path","label","img_score","txt_score","score"]]

def personalize_recs(query_top_path, cand_df, skin_profile, is_top_query=True):
    undertone = (skin_profile or {}).get("undertone", "neutral")

    if is_top_query:
        top_hsv = clothing_color_stats(query_top_path)
        cand_df = cand_df.copy()
        cand_df["item_hsv"] = cand_df["path"].apply(clothing_color_stats)
        cand_df["harmony"] = cand_df["item_hsv"].apply(lambda b: color_harmony_score(top_hsv, b))
    else:
        bottom_hsv = clothing_color_stats(query_top_path)
        cand_df = cand_df.copy()
        cand_df["item_hsv"] = cand_df["path"].apply(clothing_color_stats)
        cand_df["harmony"] = cand_df["item_hsv"].apply(lambda top_hsv: color_harmony_score(top_hsv, bottom_hsv))

    cand_df["skin"] = cand_df["item_hsv"].apply(lambda st: skin_match_score(st, undertone))
    cand_df["score2"] = 0.65*cand_df["score"] + 0.20*cand_df["harmony"] + 0.15*cand_df["skin"]
    cand_df = cand_df.sort_values("score2", ascending=False).reset_index(drop=True)
    cand_df = cand_df.rename(columns={"score2":"score"})
    return cand_df[["path","label","img_score","txt_score","harmony","skin","score"]]

CLOTHING_PROMPTS = [
    "a photo of clothing",
    "a photo of a shirt",
    "a photo of pants",
    "a fashion product photo",
]
NONCLOTHING_PROMPTS = [
    "a photo of a car",
    "a photo of a dog",
    "a photo of food",
    "a photo of a building",
    "a random object photo",
]

def _mean_text_vec(prompts):
    vecs = np.vstack([clip_text_embed_one(p) for p in prompts])
    v = vecs.mean(axis=0, keepdims=True)
    v = v / np.linalg.norm(v, axis=1, keepdims=True)
    return v.astype("float32")

cloth_vec = _mean_text_vec(CLOTHING_PROMPTS)
non_vec   = _mean_text_vec(NONCLOTHING_PROMPTS)

def is_clothing_image(img_path, margin=0.03):
    v = clip_embed_one(img_path)
    clothing_score = float(v @ cloth_vec.T)
    non_score = float(v @ non_vec.T)
    ok = clothing_score > (non_score + margin)
    return ok, clothing_score, non_score

def safe_demo_item(img_path, occasion_text="casual everyday", skin_profile=None,
                   classifier_choice="ensemble", min_class_conf=0.70, k=5):
    ok, cs, ns = is_clothing_image(img_path)
    if not ok:
        print("Rejected: does not look like clothing/person.")
        print(f"CLIP clothing_score={cs:.3f} nonclothing_score={ns:.3f}")
        return

    r, e, ens = classify_both(img_path)
    chosen = {"resnet": r, "effnet": e, "ensemble": ens}[classifier_choice]

    print("ResNet18 :", r)
    print("EffNet   :", e)
    print("Ensemble :", ens)
    print("Chosen   :", chosen)

    if chosen["conf"] < min_class_conf:
        print("Low confidence. Please upload a clearer clothing image.")
        return

    pred_label = chosen["pred_label"]

    if pred_label in TOP_CLASSES:
        recs = recommend_bottoms_free_text(img_path, occasion_text, k=k, search_k=150)
        recs = personalize_recs(img_path, recs, skin_profile, is_top_query=True)
        show_outfit_2piece(img_path, recs, title=f"TOP → Bottoms | occasion='{occasion_text}'")

    elif pred_label in BOTTOM_CLASSES:
        recs = recommend_tops_free_text(img_path, occasion_text, k=k, search_k=150)
        recs = personalize_recs(img_path, recs, skin_profile, is_top_query=False)
        show_outfit_2piece(img_path, recs, title=f"BOTTOM → Tops | occasion='{occasion_text}'")

    else:
        print("Predicted class not mapped to TOP/BOTTOM. (This dataset has no shoes.)")

import cv2, numpy as np
from facenet_pytorch import MTCNN

mtcnn = MTCNN(keep_all=True, device=device)

def clamp(v, lo, hi):
    return max(lo, min(hi, v))

def skin_mask_ycrcb(roi_bgr):
    ycrcb = cv2.cvtColor(roi_bgr, cv2.COLOR_BGR2YCrCb)
    Y, Cr, Cb = cv2.split(ycrcb)
    mask = (Cr >= 133) & (Cr <= 173) & (Cb >= 77) & (Cb <= 127)
    mask = mask & (Y > 30) & (Y < 245)
    return mask

def face_and_cheeks_bboxes(img_bgr, face_xyxy):
    h, w = img_bgr.shape[:2]
    x1, y1, x2, y2 = [int(v) for v in face_xyxy]
    x1 = clamp(x1, 0, w-2); y1 = clamp(y1, 0, h-2)
    x2 = clamp(x2, x1+2, w); y2 = clamp(y2, y1+2, h)

    bw, bh = x2-x1, y2-y1
    face = (x1, y1, bw, bh)

    cy1 = y1 + int(0.45 * bh)
    cy2 = y1 + int(0.70 * bh)

    lx1 = x1 + int(0.15 * bw); lx2 = x1 + int(0.40 * bw)
    rx1 = x1 + int(0.60 * bw); rx2 = x1 + int(0.85 * bw)

    left  = (lx1, cy1, max(2, lx2-lx1), max(2, cy2-cy1))
    right = (rx1, cy1, max(2, rx2-rx1), max(2, cy2-cy1))
    return face, left, right

def crop_xywh(img_bgr, bb):
    x,y,w,h = bb
    return img_bgr[y:y+h, x:x+w].copy()

def estimate_skin_profile_detailed(face_img_path, show=True):
    img_rgb = np.array(Image.open(face_img_path).convert("RGB"))
    img_bgr = cv2.cvtColor(img_rgb, cv2.COLOR_RGB2BGR)

    boxes, probs, landmarks = mtcnn.detect(Image.fromarray(img_rgb), landmarks=True)
    if boxes is None or len(boxes) == 0:
        raise ValueError("No face detected. Upload a clearer front-facing photo.")

    best_i = int(np.argmax(probs))
    face_xyxy = boxes[best_i]
    face_bb, left_bb, right_bb = face_and_cheeks_bboxes(img_bgr, face_xyxy)

    pixels = []
    for bb in [left_bb, right_bb]:
        roi = crop_xywh(img_bgr, bb)
        if roi.size == 0:
            continue
        mask = skin_mask_ycrcb(roi)
        if mask.sum() < 80:
            mask = np.ones(mask.shape, dtype=bool)
        pixels.append(roi[mask])

    pixels = np.vstack(pixels).astype(np.uint8)

    lab = cv2.cvtColor(pixels.reshape(-1,1,3), cv2.COLOR_BGR2LAB).reshape(-1,3)
    L_mean, a_mean, b_mean = lab.mean(axis=0)
    delta_b = b_mean - 128

    if delta_b > 6:
        undertone = "warm"
    elif delta_b < -6:
        undertone = "cool"
    else:
        undertone = "neutral"

    if L_mean >= 190: tone_detail = "very light"
    elif L_mean >= 170: tone_detail = "light"
    elif L_mean >= 135: tone_detail = "medium"
    elif L_mean >= 110: tone_detail = "tan"
    else: tone_detail = "deep"

    profile = {
        "tone_detail": tone_detail,
        "undertone": undertone,
        "L_mean": float(L_mean),
        "b_minus_128": float(delta_b),
    }

    if show:
        vis = img_rgb.copy()
        for (x,y,w,h), color, name in [
            (face_bb, (0,255,0), "face"),
            (left_bb, (255,0,0), "L cheek"),
            (right_bb, (0,0,255), "R cheek")
        ]:
            cv2.rectangle(vis, (x,y), (x+w,y+h), color, 2)
            cv2.putText(vis, name, (x, max(0,y-8)), cv2.FONT_HERSHEY_SIMPLEX, 0.6, color, 2)

        plt.figure(figsize=(6,6))
        plt.imshow(vis); plt.axis("off")
        plt.title(f"tone={tone_detail}, undertone={undertone} | b-128={delta_b:.1f}")
        plt.show()

    return profile

