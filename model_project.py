import torch.nn as nn
from sklearn.metrics import accuracy_score, f1_score
from tqdm import tqdm

@torch.no_grad()
def evaluate(model, loader):
    model.eval()
    ce = nn.CrossEntropyLoss()
    total_loss = 0.0
    ys, ps = [], []
    for x, y in loader:
        x, y = x.to(device), y.to(device)
        logits = model(x)
        loss = ce(logits, y)
        total_loss += loss.item() * x.size(0)
        pred = logits.argmax(1)
        ys.append(y.cpu()); ps.append(pred.cpu())
    ys = torch.cat(ys).numpy()
    ps = torch.cat(ps).numpy()
    return (total_loss / len(loader.dataset),
            accuracy_score(ys, ps),
            f1_score(ys, ps, average="macro"))

def train_model(model, train_loader, val_loader, epochs=5, lr=3e-4, save_path="/content/resnet18_best.pt"):
    model.to(device)
    opt = torch.optim.AdamW(model.parameters(), lr=lr, weight_decay=1e-4)
    scaler = torch.cuda.amp.GradScaler(enabled=(device=="cuda"))
    ce = nn.CrossEntropyLoss()

    best_mf1 = -1.0

    for ep in range(1, epochs+1):
        model.train()
        running = 0.0

        for x, y in tqdm(train_loader, desc=f"Epoch {ep}/{epochs}"):
            x, y = x.to(device), y.to(device)
            opt.zero_grad(set_to_none=True)

            with torch.cuda.amp.autocast(enabled=(device=="cuda")):
                logits = model(x)
                loss = ce(logits, y)

            scaler.scale(loss).backward()
            scaler.step(opt)
            scaler.update()

            running += loss.item() * x.size(0)

        train_loss = running / len(train_loader.dataset)
        val_loss, val_acc, val_mf1 = evaluate(model, val_loader)
        print(f"Epoch {ep}: train_loss={train_loss:.4f} | val_loss={val_loss:.4f} | val_acc={val_acc:.4f} | val_macroF1={val_mf1:.4f}")

        if val_mf1 > best_mf1:
            best_mf1 = val_mf1
            torch.save({"model": model.state_dict()}, save_path)
            print("  saved best ->", save_path)

    print("Best val macro-F1:", best_mf1)
    return best_mf1

