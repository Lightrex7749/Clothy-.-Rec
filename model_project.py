def recommend_by_category(query_img_path, target_classes, top_k=5, search_k=80):
    """
    Search FAISS globally, then filter results to target_classes.
    search_k should be > top_k to allow filtering.
    """
    q = clip_embed_one(query_img_path)
    scores, idxs = index_all.search(q, search_k)

    recs = meta.iloc[idxs[0]].copy()
    recs["score"] = scores[0]

    # filter by category + remove same image
    recs = recs[recs["label"].isin(target_classes)]
    recs = recs[recs["path"] != query_img_path]

    return recs.head(top_k).reset_index(drop=True)

