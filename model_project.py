def recommend_bottoms_hybrid(top_img_path, occasion="casual", k=5, search_k=50,
                             w_img=0.75, w_txt=0.25):
    """
    w_img: weight for image-image compatibility (CLIP image similarity)
    w_txt: weight for bottom matching the occasion prompt (CLIP text similarity)
    """
    # 1) retrieve candidate bottoms by image similarity
    q_img = clip_embed_one(top_img_path)  # (1,512)
    img_scores, idxs = index_bottoms.search(q_img, search_k)

    picked_meta_rows = bottoms_meta_idx[idxs[0]]
    cand = meta.iloc[picked_meta_rows].copy()
    cand["img_score"] = img_scores[0]

    # 2) text score: compare candidate bottom image embeddings to averaged prompt embedding
    occ = occasion.lower().strip()
    if occ not in text_embeds:
        occ = "casual"

    prompt_vec = text_embeds[occ].mean(axis=0, keepdims=True)  # (1,512), already normalized-ish
    prompt_vec = prompt_vec / np.linalg.norm(prompt_vec, axis=1, keepdims=True)

    cand_emb = emb_all[picked_meta_rows]  # (search_k,512)
    txt_score = (cand_emb @ prompt_vec.T).reshape(-1)  # cosine because normalized
    cand["txt_score"] = txt_score

    # 3) combine
    cand["final_score"] = w_img*cand["img_score"] + w_txt*cand["txt_score"]
    cand = cand.sort_values("final_score", ascending=False).head(k).reset_index(drop=True)
    return cand[["path","label","img_score","txt_score","final_score"]]

