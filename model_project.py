def color_family_from_hsv(h, s, v):
    # neutral if low saturation (gray/black/white/denim-like)
    if s < 45:
        return "neutral"
    # warm hues: red/orange/yellow/olive-ish
    if (h <= 35) or (h >= 160) or (35 < h <= 80):
        return "warm"
    # otherwise treat as cool (green/cyan/blue/purple range)
    return "cool"

def skin_match_score(item_hsv_stats, undertone):
    """
    Returns 0..1 score.
    Neutral colors work for everyone, so they score high.
    """
    h, s, v = item_hsv_stats
    fam = color_family_from_hsv(h, s, v)

    if fam == "neutral":
        return 0.95
    if undertone == "neutral":
        return 0.80
    return 1.00 if fam == undertone else 0.45

def recommend_bottoms_personalized(top_img_path, occasion_text, skin_profile, k=5, search_k=120,
                                  w_clip=0.60, w_harmony=0.20, w_skin=0.20):
    """
    Uses:
    - recommend_bottoms_free_text() -> gives img_score/txt_score/final_score (CLIP image+text)
    - color_harmony_score(top,bottom)
    - skin_match_score(bottom, user undertone)
    """
    # 1) base candidates from CLIP (image+text)
    cand = recommend_bottoms_free_text(top_img_path, occasion_text, k=max(k, 10), search_k=search_k).copy()

    # 2) compute bottom HSV stats
    cand["bottom_hsv"] = cand["path"].apply(clothing_color_stats)

    # 3) harmony with the top
    top_hsv = clothing_color_stats(top_img_path)
    cand["harmony_score"] = cand["bottom_hsv"].apply(lambda b: color_harmony_score(top_hsv, b))

    # 4) skin-tone match
    undertone = skin_profile["undertone"]
    cand["skin_score"] = cand["bottom_hsv"].apply(lambda st: skin_match_score(st, undertone))

    # 5) final score (combine)
