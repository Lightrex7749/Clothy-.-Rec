import cv2, numpy as np
from sklearn.cluster import KMeans
from PIL import Image

def dominant_rgb(img_path, k=3, resize=160):
    img = np.array(Image.open(img_path).convert("RGB"))
    h, w = img.shape[:2]
    scale = resize / max(h, w)
    img = cv2.resize(img, (int(w*scale), int(h*scale)))
    X = img.reshape(-1, 3)

    km = KMeans(n_clusters=k, n_init="auto", random_state=42).fit(X)
    centers = km.cluster_centers_.astype(int)
    counts = np.bincount(km.labels_)
    dom = centers[counts.argmax()]
    return dom  # [R,G,B]

def rgb_to_hsv(rgb):
    rgb = np.uint8([[rgb]])
    hsv = cv2.cvtColor(rgb, cv2.COLOR_RGB2HSV)[0][0]
    return hsv  # [H(0-179), S(0-255), V(0-255)]

def color_harmony_score(rgb1, rgb2):
    # simple hue distance + preference for neutral bottoms
    h1,s1,v1 = rgb_to_hsv(rgb1)
    h2,s2,v2 = rgb_to_hsv(rgb2)
    hue_dist = min(abs(int(h1)-int(h2)), 180-abs(int(h1)-int(h2))) / 90.0  # 0..1 roughly
    neutral_bonus = 0.2 if s2 < 50 else 0.0  # low saturation bottom = neutral
    return float(1.0 - hue_dist + neutral_bonus)

