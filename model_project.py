import numpy as np

def recommend_tops_free_text(bottom_img_path, occasion_text, k=5, search_k=120, w_img=0.70, w_txt=0.30):
    occasion_text = (occasion_text or "").strip()
    if len(occasion_text) == 0:
        occasion_text = "casual everyday"

    # 1) retrieve candidate tops by image similarity
    q_img = clip_embed_one(bottom_img_path)
    img_scores, idxs = index_tops.search(q_img, search_k)

    picked_meta_rows = tops_meta_idx[idxs[0]]
    cand = meta.iloc[picked_meta_rows].copy()
    cand["img_score"] = img_scores[0]

    # 2) text score using templates (tops-focused)
    templates = [
        f"men's {occasion_text} shirt",
        f"men's {occasion_text} t-shirt",
        f"men's {occasion_text} hoodie",
    ]
    t = np.mean([clip_text_embed_one(t) for t in templates], axis=0)  # (1,512)
    t = t / np.linalg.norm(t, axis=1, keepdims=True)

    cand_emb = emb_all[picked_meta_rows]
    cand["txt_score"] = (cand_emb @ t.T).reshape(-1)

    # 3) combine
    cand["final_score"] = w_img*cand["img_score"] + w_txt*cand["txt_score"]
    cand = cand.sort_values("final_score", ascending=False).head(k).reset_index(drop=True)
    return cand[["path","label","img_score","txt_score","final_score"]]

def recommend_tops_personalized(bottom_img_path, occasion_text, skin_profile, k=5, search_k=150,
                               w_clip=0.60, w_harmony=0.20, w_skin=0.20):
    cand = recommend_tops_free_text(bottom_img_path, occasion_text, k=max(k, 10), search_k=search_k).copy()

    # color stats
    cand["top_hsv"] = cand["path"].apply(clothing_color_stats)
    bottom_hsv = clothing_color_stats(bottom_img_path)

    # harmony: treat candidate top as "top" and query as "bottom"
    cand["harmony_score"] = cand["top_hsv"].apply(lambda top_hsv: color_harmony_score(top_hsv, bottom_hsv))

    # skin match for candidate top
    undertone = skin_profile["undertone"]
    cand["skin_score"] = cand["top_hsv"].apply(lambda st: skin_match_score(st, undertone))

    cand["final_personal"] = (
        w_clip * cand["final_score"] +
        w_harmony * cand["harmony_score"] +
        w_skin * cand["skin_score"]
    )

    cand = cand.sort_values("final_personal", ascending=False).head(k).reset_index(drop=True)
    return cand

