import cv2, numpy as np
from PIL import Image

def clothing_color_stats(img_path, resize=180):
    """
    Returns (h,s,v) mean of 'non-background' pixels in HSV.
    We drop near-white/low-saturation pixels to avoid background dominating.
    """
    img = np.array(Image.open(img_path).convert("RGB"))
    h, w = img.shape[:2]
    scale = resize / max(h, w)
    img = cv2.resize(img, (int(w*scale), int(h*scale)))

    hsv = cv2.cvtColor(img, cv2.COLOR_RGB2HSV)
    H, S, V = hsv[:,:,0], hsv[:,:,1], hsv[:,:,2]

    # keep pixels that are not "white background"
    mask = (S > 35) & (V < 245)  # tune if needed
    if mask.sum() < 200:         # fallback if mask too small
        mask = np.ones_like(S, dtype=bool)

    h_mean = float(H[mask].mean())
    s_mean = float(S[mask].mean())
    v_mean = float(V[mask].mean())
    return h_mean, s_mean, v_mean

def hue_dist(h1, h2):
    d = abs(h1 - h2)
    return min(d, 180 - d)  # hue is 0..179 in OpenCV

def color_harmony_score(top_stats, bottom_stats):
    """
    Simple stylist rule:
    - If top is colorful (high saturation), prefer neutral bottoms (low saturation)
    - If top is neutral, allow more color
    - Prefer some value contrast (not same brightness)
    Returns score ~0..1+
    """
    ht, st, vt = top_stats
    hb, sb, vb = bottom_stats

    # bottom neutrality bonus (good for most outfits)
    neutral_bonus = 1.0 - min(sb/255.0, 1.0)  # low saturation => higher bonus

    # if top is colorful, strongly prefer neutral bottoms
    top_colorful = (st > 90)
    if top_colorful:
        neutral_bonus = neutral_bonus * 1.3

    # brightness contrast (avoid too similar brightness)
    v_contrast = min(abs(vt - vb)/255.0, 1.0)

    # mild hue penalty (only matters when bottoms are also colorful)
    hue_penalty = (hue_dist(ht, hb)/90.0) * (sb/255.0)  # colorful bottom => penalty matters more

    score = 0.55*neutral_bonus + 0.35*v_contrast - 0.20*hue_penalty
    return float(score)

