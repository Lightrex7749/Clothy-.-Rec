@torch.no_grad()
def clip_embed_paths(paths, batch_size=64):
    embs = []
    for i in tqdm(range(0, len(paths), batch_size)):
        batch_paths = paths[i:i+batch_size]
        imgs = [clip_preprocess(Image.open(p).convert("RGB")) for p in batch_paths]
        x = torch.stack(imgs).to(device)
        f = clip_model.encode_image(x)
        f = f / f.norm(dim=-1, keepdim=True)          # normalize for cosine similarity
        embs.append(f.cpu().numpy().astype("float32"))
    return np.vstack(embs)

