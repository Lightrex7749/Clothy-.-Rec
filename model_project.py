@torch.no_grad()
def classify_both(img_path):
    x = eval_tf(Image.open(img_path).convert("RGB")).unsqueeze(0).to(device)

    lr = resnet(x)
    le = effnet(x)

    pr = torch.softmax(lr, 1)[0].detach().cpu().numpy()
    pe = torch.softmax(le, 1)[0].detach().cpu().numpy()
    pens = torch.softmax((lr + le) / 2.0, 1)[0].detach().cpu().numpy()

    def pack(p):
        pred_id = int(p.argmax())
        return {
            "pred_id": pred_id,
            "pred_label": id2label[pred_id],
            "conf": float(p.max()),
            "probs": p
        }

    return pack(pr), pack(pe), pack(pens)

@torch.no_grad()
def clip_embed_one(path):
    x = clip_preprocess(Image.open(path).convert("RGB")).unsqueeze(0).to(device)
    f = clip_model.encode_image(x)
    f = f / f.norm(dim=-1, keepdim=True)
