def style_from_person_photo(person_path, occasion_text="casual everyday", use_skin=True, k=5):
    # 1) Pose crop (TOP/BOTTOM)
    import cv2, numpy as np
    from ultralytics import YOLO

    # load pose model once (cache it)
    global pose_model
    if "pose_model" not in globals() or pose_model is None:
        pose_model = YOLO("yolov8n-pose.pt")

    img_bgr = cv2.imread(person_path)
    if img_bgr is None:
        raise ValueError("Could not read person image path.")

    H, W = img_bgr.shape[:2]
    res = pose_model.predict(person_path, conf=0.25, verbose=False)[0]
    if res.keypoints is None or len(res.keypoints) == 0:
        raise ValueError("No person keypoints detected. Upload a clearer full-body standing photo.")

    boxes_conf = res.boxes.conf.cpu().numpy()
    best_i = int(np.argmax(boxes_conf))
    kpts = res.keypoints.xy[best_i].cpu().numpy()

    # COCO indices
    L_SH, R_SH = 5, 6
    L_HIP, R_HIP = 11, 12
    L_ANK, R_ANK = 15, 16

    def clamp(v, lo, hi):
        return int(max(lo, min(hi, v)))

    def safe_xy(points):
        pts = points.copy()
        good = ~np.isnan(pts[:,0]) & ~np.isnan(pts[:,1]) & (pts[:,0] > 1) & (pts[:,1] > 1)
        return pts[good] if good.any() else pts

    shoulders = safe_xy(kpts[[L_SH, R_SH]])
    hips      = safe_xy(kpts[[L_HIP, R_HIP]])
    ankles    = safe_xy(kpts[[L_ANK, R_ANK]])

    x1, y1, x2, y2 = res.boxes.xyxy[best_i].cpu().numpy()
    x1, y1, x2, y2 = map(int, [x1,y1,x2,y2])

    if shoulders.size and hips.size:
        top_y1 = min(shoulders[:,1].min(), hips[:,1].min())
        top_y2 = hips[:,1].max()
        top_x1 = min(shoulders[:,0].min(), hips[:,0].min())
        top_x2 = max(shoulders[:,0].max(), hips[:,0].max())
    else:
        top_x1, top_y1, top_x2, top_y2 = x1, y1, x2, int((y1+y2)/2)

    if hips.size and ankles.size:
        bot_y1 = hips[:,1].min()
        bot_y2 = ankles[:,1].max()
        bot_x1 = min(hips[:,0].min(), ankles[:,0].min())
        bot_x2 = max(hips[:,0].max(), ankles[:,0].max())
    else:
        bot_x1, bot_y1, bot_x2, bot_y2 = x1, int((y1+y2)/2), x2, y2

    pad_x = int(0.10 * (x2 - x1))
    pad_y = int(0.08 * (y2 - y1))

    top_x1 = clamp(top_x1 - pad_x, 0, W-1); top_x2 = clamp(top_x2 + pad_x, 0, W-1)
    top_y1 = clamp(top_y1 - pad_y, 0, H-1); top_y2 = clamp(top_y2 + pad_y, 0, H-1)

    bot_x1 = clamp(bot_x1 - pad_x, 0, W-1); bot_x2 = clamp(bot_x2 + pad_x, 0, W-1)
    bot_y1 = clamp(bot_y1 - pad_y, 0, H-1); bot_y2 = clamp(bot_y2 + pad_y, 0, H-1)

    top_crop = img_bgr[top_y1:top_y2, top_x1:top_x2].copy()
    bot_crop = img_bgr[bot_y1:bot_y2, bot_x1:bot_x2].copy()

    top_crop_path = "/content/top_crop.jpg"
    bot_crop_path = "/content/bottom_crop.jpg"
    cv2.imwrite(top_crop_path, top_crop)
    cv2.imwrite(bot_crop_path, bot_crop)

    # 2) Skin profile (optional)
    skin_used = {"undertone": "neutral"}
    if use_skin:
        try:
            skin_used = estimate_skin_profile_detailed(person_path, show=False)
        except Exception:
            # fallback to whatever you already computed earlier if available
            if "skin_profile_detailed" in globals():
                skin_used = skin_profile_detailed

    print("Occasion:", occasion_text)
    print("Skin:", skin_used.get("skin_description", f"undertone={skin_used.get('undertone','?')}"))

    # 3) Recommend bottoms from TOP crop
    print("\n--- TOP crop → bottoms ---")
    safe_demo(top_crop_path, occasion_text=occasion_text, skin_profile=skin_used, classifier_choice="ensemble", reco_k=k)

    # 4) Recommend tops from BOTTOM crop
    print("\n--- BOTTOM crop → tops ---")
    safe_demo(bot_crop_path, occasion_text=occasion_text, skin_profile=skin_used, classifier_choice="ensemble", reco_k=k)

    return top_crop_path, bot_crop_path, skin_used

