def recommend_bottoms_free_text(top_img_path, occasion_text, k=5, search_k=80,
                                w_img=0.70, w_txt=0.30):
    """
    occasion_text: user input like 'wedding', 'office', 'gym', 'date night', etc.
    """
    occasion_text = (occasion_text or "").strip()
    if len(occasion_text) == 0:
        occasion_text = "casual everyday"

    # 1) retrieve candidate bottoms by image similarity
    q_img = clip_embed_one(top_img_path)
    img_scores, idxs = index_bottoms.search(q_img, search_k)

    picked_meta_rows = bottoms_meta_idx[idxs[0]]
    cand = meta.iloc[picked_meta_rows].copy()
    cand["img_score"] = img_scores[0]

    # 2) text score: use templates to make the prompt more clothing-specific
    templates = [
        f"men's {occasion_text} outfit pants",
        f"men's {occasion_text} trousers",
        f"men's {occasion_text} jeans outfit",
    ]
    t = np.mean([clip_text_embed_one(t) for t in templates], axis=0)  # (1,512)
